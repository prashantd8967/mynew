public class Lamb4 {
	
	public String name;
	
	public Long salary;
	
	public String city;

	public Lamb4(String name, Long salary, String city) {
		super();
		this.name = name;
		this.salary = salary;
		this.city = city;
	}

	@Override
	public String toString() {
		return "Lamb4 [name=" + name + ", salary=" + salary + ", city=" + city + "]";
	}
	
	

}

 

